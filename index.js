// ----- Bot WhatsApp -----
const baileys = require("@whiskeysockets/baileys");
const { default: makeWASocket, DisconnectReason, fetchLatestBaileysVersion } = baileys;
// Correct import de useSingleFileAuthState
const { useSingleFileAuthState } = require("@whiskeysockets/baileys/lib/Utils/auth_state");
const P = require("pino");
const fs = require("fs");
const path = require("path");

// Pr√©fixe des commandes
const PREFIX = "!";

// Authentification QR code
const { state, saveState } = useSingleFileAuthState(path.resolve(__dirname, 'auth_info.json'));

async function startBot() {
    const { version } = await fetchLatestBaileysVersion();

    const sock = makeWASocket({
        version,
        auth: state,
        logger: P({ level: 'silent' })
    });

    // Sauvegarde automatique des credentials
    sock.ev.on('creds.update', saveState);

    // Gestion de la connexion et du QR code
    sock.ev.on('connection.update', (update) => {
        const { connection, lastDisconnect, qr } = update;

        if (qr) {
            console.log("üì≤ Scanner ce QR code avec WhatsApp :", qr);
        }

        if (connection === 'close') {
            const shouldReconnect = lastDisconnect.error?.output?.statusCode !== DisconnectReason.loggedOut;
            console.log('‚ùå Connexion ferm√©e, reconnect ?', shouldReconnect);
            if (shouldReconnect) startBot();
        } else if (connection === 'open') {
            console.log('‚úÖ Connect√© au compte WhatsApp !');
        }
    });

    // Chargement dynamique des commandes
    const commands = new Map();
    const commandFiles = fs.existsSync(path.join(__dirname, "commands"))
        ? fs.readdirSync(path.join(__dirname, "commands")).filter(f => f.endsWith(".js"))
        : [];
    for (const file of commandFiles) {
        const command = require(`./commands/${file}`);
        if (command.name) commands.set(command.name, command);
    }

    // Gestion des messages entrants
    sock.ev.on("messages.upsert", async (m) => {
        const msg = m.messages[0];
        if (!msg.key.fromMe && msg.message?.conversation) {
            const text = msg.message.conversation;
            if (text.startsWith(PREFIX)) {
                const args = text.slice(PREFIX.length).trim().split(/ +/);
                const cmdName = args.shift().toLowerCase();
                const command = commands.get(cmdName);
                if (command) {
                    try {
                        await command.run({ sock, msg, args });
                    } catch (err) {
                        console.error(err);
                        await sock.sendMessage(msg.key.remoteJid, { text: "Erreur lors de l'ex√©cution de la commande." });
                    }
                }
            }
        }
    });
}

startBot().catch(console.error);